void main() {
  int a = 5;
  int b = 10;

  // Operasi perkalian
  int perkalian = a * b;

  // Operasi penjumlahan
  int penjumlahan = a + b;

  // Operasi pengurangan
  int pengurangan = a - b;

  // Operasi pembagian (dalam bentuk double)
  double pembagian = a / b;

  // Tampilkan hasil operasi
  print('$a * $b = $perkalian');
  print('$a + $b = $penjumlahan');
  print('$a - $b = $pengurangan');
  print('$a / $b = $pembagian');
}
